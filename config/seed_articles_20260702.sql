## 先把问题分层

一个可靠的 AI 应用至少有四层：

- 模型层：负责生成、总结、分类、推理。
- 检索层：负责把站内文档、数据库记录、业务知识找出来。
- 工具层：负责真正执行动作，例如查询订单、创建文章、发送通知。
