## 记忆不是无限上下文

会话记忆应该保存的是可复用事实，而不是把所有聊天记录都塞回上下文。比如用户偏好、常用入口、项目背景可以长期保存；一次性的闲聊、重复确认、无效报错不应该长期污染记忆。

