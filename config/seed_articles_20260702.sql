## 1. 构建目标要和服务器一致

如果线上是 ARM 设备，构建脚本就不能默认 amd64。构建脚本应该允许用环境变量覆盖，同时默认值贴近真实部署环境。

## 2. 配置和密钥必须分离

仓库里只保留模板，例如 config.yml.tpl 或 .env.example。真实数据库密码、对象存储密钥、邮件密钥都放在线上配置或环境变量里。

## 3. 启动日志要少而准

生产环境不应该刷满 SQL 细节。日志要重点记录启动端口、运行模式、关键依赖是否可用、请求耗时和错误原因。

